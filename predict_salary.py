import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
sal=pd.read_csv('data.csv')
sal.head()

x=pd.DataFrame(sal['YearsExperience'])
y=pd.DataFrame(sal['Salary'])

from sklearn.model_selection import train_test_split
x_train,x_test,y_train,y_test=train_test_split(x,y,train_size=0.20)

from sklearn.linear_model import LinearRegression
cls=LinearRegression()
cls.fit(x_train,y_train)
y_pred=cls.predict(x_test)
plt.scatter(x_train,y_train,color='m',marker='o')
plt.plot(x_test,y_pred,color='g')
plt.xlabel('Years of Experience')                 #labeling x cordinate
plt.ylabel('Salary')                 #labeling y cordinate
plt.show()
#print("Accuracy %d",cls.score(x_test,y_test)) # The R2 score

experience=int(input("Enter Number of experience"))
y_prediction=cls.predict([[experience]])
print(y_prediction)
